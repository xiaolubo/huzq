package com.mrkj.data;

public class Sdkprice {

	private Integer id;
	private String name;
	private String operatorName;
	private String provinceName;
	private Integer price;
	private Integer level;
	private Integer isshield;
	
	public Sdkprice(Integer id,String name,String operatorName,String provinceName,Integer price,Integer level,Integer isshield){
		this.id = id;
		this.name = name;
		this.operatorName = operatorName;
		this.provinceName = provinceName;
		this.price = price;
		this.level = level;
		this.isshield = isshield;
	}
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getOperatorName() {
		return operatorName;
	}
	public void setOperatorName(String operatorName) {
		this.operatorName = operatorName;
	}
	public String getProvinceName() {
		return provinceName;
	}
	public void setProvinceName(String provinceName) {
		this.provinceName = provinceName;
	}
	public Integer getPrice() {
		return price;
	}
	public void setPrice(Integer price) {
		this.price = price;
	}
	public Integer getLevel() {
		return level;
	}
	public void setLevel(Integer level) {
		this.level = level;
	}
	public Integer getIsshield() {
		return isshield;
	}
	public void setIsshield(Integer isshield) {
		this.isshield = isshield;
	}
	
	
	
}
